<b>CLÁUSULA QUARTA – RESCISÃO DE CONTRATO</b><br/>
4. Este contrato poderá ser rescindido nas seguintes situações:<br/>
4.1. Pelo(a) CONTRATANTE, no caso de o(a) ALUNO-CONTRATANTE(A):<br/>
a) não renovar a matrícula nos prazos estabelecidos pela Faculdade, em Edital;<br/>
b) se transferir para outra instituição de ensino superior. A transferência deverá ser requerida à Diretoria de Admissão e
Registro (D.A.R.);<br/>
c)
trancar a matrícula. O trancamento deve ser requerido, através de protocolo, à Diretoria de Admissão e Registro
(D.A.R.). A matrícula poderá ser reaberta de acordo com os
prazos e demais normas internas da Faculdade.<br/>
4.2. Pela CONTRATADA, no caso de:<br/>
A)
Desligamento do(a) ALUNO-CONTRATANTE(A) de acordo com os termos do Estatuto, do Regimento Geral e das Normas Internas da
ISCON-DF.<br/>
B) inadimplência, nos termos deste Contrato.<br/>
4.1.1. RESCISÃO - A prestação de serviços educacionais objeto deste Contrato, poderá ser rescindida: a) em virtude de
cancelamento da matrícula, transferência de instituição ou
trancamento do curso, conforme estipulado neste Contrato, no Regimento da CONTRATADA, os quais compõem este instrumento
e são do conhecimento do CONTRATANTE; b) por
acordo entre as partes; c) caso o(a) CONTRATANTE tenha débitos referentes a quaisquer outros contratos firmados com a
ISCON- CONTRATADA, anteriores ou posteriores a
confirmação do presente Contrato, em estrita observância ao art. 5º da Lei 9.870/99; d) por infração disciplinar
prevista no Código de Ética e/ou Regimento Interno, parte integrante e
inseparável deste Contrato, que justifique, nos termos deste, e da legislação pertinente, seu desligamento do
estabelecimento de ensino.<br/>
4.3. Em caso de rescisão, o(a) CONTRATANTE fica obrigado a saldar o valor do(s) débito(s) eventualmente existente(s). <br/>
4.4.
Em caso de cancelamento de matrícula de CONTRATANTE, a FACULDADE ISCON considerará a data prevista no Manual do Candidato
ao Concurso Vestibular. Se o
cancelamento for solicitado até a data do início das aulas, o candidato poderá solicitar a devolução de 50%(CINQUENTA
por cento) do valor das parcelas pagas da semestralidade do período
ficando os 50% CINQUENTA por cento) restantes retidos a fim de cobrir despesas administrativas da IES. Após o início das
aulas, não haverá devolução possível.<br/>
4.5. – Em caso de trancamento de matrícula após o início do período letivo, não haverá devolução das parcelas pagas.
O(A) ALUNO-CONTRATANTE(A) poderá informar à
CONTRATADA, mediante requerimento junto ao Portal do Aluno, obedecidos os prazos que o calendário escolar estabelece. A
rescisão do presente contrato ou trancamento da matrícula,
solicitado após o término do prazo estabelecido no Calendário Acadêmico, obriga o requerente-aluno, ao pagamento de
multa de 10% (dez por cento) – a título de cláusula penal - sobre o
valor bruto das parcelas vincendas, além do pagamento das parcelas vencidas e não quitadas.<br/>
4.6.
Nas hipóteses de requerimento de Trancamento da Matrícula, não haverá restituição de valores pagos, pois o TRANCAMENTO
consiste na guarda temporária de vaga no curso
em questão, cujo período limite de reserva da vaga consta no Regimento Geral da IES, devendo, entretanto, formular
requerimento eletrônico, neste sentido, na Secretaria Acadêmica /
Portal do ALUNO-CONTRATANTE ISCON, com preenchimento e assinatura, com reconhecimento de firma, de termo específico
disponibilizado pela CONTRATADA, podendo ser
solicitado conforme calendário acadêmico, respeitando os critérios estabelecidos nos respectivos Editais do Processo
Seletivo.
